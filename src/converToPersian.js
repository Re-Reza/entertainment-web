export function convertToPersian(value){
    switch(value)
    {
        case"action":
            return "اکشن";

        case "animation":
            return "انيميشن";

        case"comedy":
        return"کمدی";

        case"documentary":
        return"مستند";

        case"drama":
        return"عاشقانه";

        case"family":
        return"خانوادگی";

        case"historical":
        return"تاریخی";

        case"horrific":
        return"وحشت";

        case"imaginary":
        return"علمی تخیلی";

        case "movies":
            return"فیلم";
        case "series":
            return "سریال";
        case "all":
            return "همه"

    }
}

